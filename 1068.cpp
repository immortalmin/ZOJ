int main() {
//	freopen("1.txt","r",stdin);
	m1['A']=".-",m1['H']="....",m1['O']="---",m1['V']="...-";
	m1['B']="-...",m1['I']="..",m1['P']=".--.",m1['W']=".--";
	m1['C']="-.-.",m1['J']=".---",m1['Q']="--.-",m1['X']="-..-";
	m1['D']="-..",m1['K']="-.-",m1['R']=".-.",m1['Y']="-.--";
	m1['E']=".",m1['L']=".-..",m1['S']="...",m1['Z']="--..";
	m1['F']="..-.",m1['M']="--",m1['T']="-";
	m1['G']="--.",m1['N']="-.",m1['U']="..-";
	m1['_']="..--",m1['.']="---.",m1[',']=".-.-",m1['?']="----";
	
	m2[".-"]='A',m2["...."]='H',m2["---"]='O',m2["...-"]='V';
	m2["-..."]='B',m2[".."]='I',m2[".--."]='P',m2[".--"]='W';
	m2["-.-."]='C',m2[".---"]='J',m2["--.-"]='Q',m2["-..-"]='X';
	m2["-.."]='D',m2["-.-"]='K',m2[".-."]='R',m2["-.--"]='Y';
	m2["."]='E',m2[".-.."]='L',m2["..."]='S',m2["--.."]='Z';
	m2["..-."]='F',m2["--"]='M',m2["-"]='T';
	m2["--."]='G',m2["-."]='N',m2["..-"]='U';
	m2["..--"]='_',m2["---."]='.',m2[".-.-"]=',',m2["----"]='?';
	int n;
	cin>>n;
	string s;
	for(int t=1;t<=n;t++){
		cin>>s;
		string s2="";
		int a[105],k=0;
		for(int i=0;i<s.size();i++){
			s2+=m1[s[i]];
			a[k++]=m1[s[i]].size();
		}
		reverse(a,a+k);
		int index=0;
		s="";
		for(int i=0;i<k;i++){
			s+=m2[s2.substr(index,a[i])];
			index+=a[i];
		}
		cout<<t<<": "<<s<<endl;
	}
	return 0;
}
